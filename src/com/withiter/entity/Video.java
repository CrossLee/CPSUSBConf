package com.withiter.entity;


public class Video {
	
	public String name;
	public String ext;
	public String path;
	public String size;
	public String updateTime;
	
	public Video(String name, String ext, String path, String size,
			String updateTime) {
		super();
		this.name = name;
		this.ext = ext;
		this.path = path;
		this.size = size;
		this.updateTime = updateTime;
	}
	
	
}
